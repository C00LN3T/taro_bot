from __future__ import annotations

import random

RUNES = {
    "Феху": "Ресурс, старт, возможность приумножить ценность.",
    "Уруз": "Сила, здоровье, устойчивость.",
    "Ансуз": "Слово, вдохновение, обучение.",
    "Гебо": "Обмен, партнёрство, союз.",
    "Вуньо": "Радость, облегчение, гармония.",
}

METAPHORS = [
    "Когда туман рассеется, путь будет проще, но идти нужно уже сейчас.",
    "То, что кажется стеной, иногда всего лишь дверь, которую ещё не попробовали открыть.",
    "Сила воды — в мягкости и настойчивости одновременно.",
    "Сделайте полшага вперёд, чтобы услышать, как отвечает жизнь.",
]


def rune_of_the_day() -> tuple[str, str]:
    name = random.choice(list(RUNES.keys()))
    return name, RUNES[name]


def metaphor_of_the_day() -> str:
    return random.choice(METAPHORS)
